use std::collections::{BTreeMap, BTreeSet};
use std::path::{Path, PathBuf};

use cu::pre::*;
use itertools::Itertools as _;

use super::kebab;
use super::parse::{ModuleData, ModuleFileStructure, ParsedModule};
use super::platform::{CfgAttr, Target, TargetSet};

pub struct RegistryBuilder {
    registry_src_path: PathBuf,
    packages: BTreeMap<String, ModuleFileStructure>,
}
impl RegistryBuilder {
    pub fn new(path: PathBuf) -> Self {
        Self {
            registry_src_path: path,
            packages: Default::default(),
        }
    }
    pub fn add(&mut self, structure: ModuleFileStructure) -> cu::Result<()> {
        use std::collections::btree_map::Entry;
        match self.packages.entry(structure.package_name.clone()) {
            Entry::Vacant(e) => {
                e.insert(structure);
            }
            Entry::Occupied(_) => {
                cu::bail!(
                    "multiple location found for package '{}', please consolidate to the same location",
                    structure.package_name
                );
            }
        }
        Ok(())
    }
    pub fn build(self) -> cu::Result<String> {
        use std::fmt::Write as _;

        let mut out = format!(
            r##"
// This file is generated by the build script, do not edit manually.

#![allow(non_snake_case)]
#![allow(non_camel_case_types)]
#![allow(unused_braces)]
#![allow(clippy::should_implement_trait)]
#![allow(clippy::non_minimal_cfg)]

{target_attr}
compile_error!("current target triple is not supported");

use enumset::{{EnumSetType, EnumSet, enum_set}};
use enum_map::Enum;
mod _stub {{ {stub_impl} }}
"##,
            target_attr = CfgAttr(TargetSet::new()).attr(),
            stub_impl = include_str!("./stub_impl.rs")
        );

        // if the package name contains invalid character,
        // there will be build error in the generated code.
        // the name is not validated here
        // (it should be rare since the registry is fully controlled by me)

        let kebab_pkgs = self.packages.keys().collect::<Vec<_>>();
        let (pascal_pkgs, snake_pkgs) = kebab::generate_casings_from_kebab(&kebab_pkgs);
        cu::check!(
            generate_id_enum(&mut out, "Pkg", &kebab_pkgs, &pascal_pkgs),
            "failed to generate PkgId enum"
        )?;

        let mut all_binaries = BTreeSet::default();
        let mut modules = Vec::with_capacity(kebab_pkgs.len());

        for (key, path) in &self.packages {
            let m = cu::check!(ParsedModule::parse(path), "failed to parse module '{key}'")?;
            m.collect_binaries(&mut all_binaries);
            modules.push(m);
        }

        let kebab_bins = all_binaries.iter().collect::<Vec<_>>();
        let (pascal_bins, _) = kebab::generate_casings_from_kebab(&kebab_bins);
        cu::check!(
            generate_id_enum(&mut out, "Bin", &kebab_bins, &pascal_bins),
            "failed to generate BinId enum"
        )?;

        let metadatas = create_metadata(&modules)?;

        // metadata table
        let _ = writeln!(out, "static METADATA_ARRAY: &[crate::Package] = &[");
        for (snake_name, (kebab_name, metadata)) in
            std::iter::zip(&snake_pkgs, std::iter::zip(&kebab_pkgs, &metadatas))
        {
            for m in metadata {
                m.build_metadata(snake_name, kebab_name, &mut out)?;
            }
        }
        let _ = writeln!(out, "];");

        out += r##"impl PkgId {
    /// Get package metadata
    pub fn package(self) -> &'static crate::Package {{ &METADATA_ARRAY[Enum::into_usize(self)] }}
}

impl BinId {
    /// Get packages that provide this binary
    pub fn providers(self) -> EnumSet<PkgId> {
        match self {
"##;
        build_bin_providers(&pascal_bins, &kebab_bins, &pascal_pkgs, &modules, &mut out)?;
        out += r##"}
    }
}"##;

        for (snake_name, metadata) in std::iter::zip(&snake_pkgs, &metadatas) {
            for m in metadata {
                m.build_module(snake_name, &self.registry_src_path, &mut out)?;
            }
        }

        Ok(out)
    }
}

fn create_metadata(modules: &[ParsedModule]) -> cu::Result<Vec<Vec<ModuleMetadata<'_>>>> {
    let mut out = Vec::with_capacity(modules.len());

    for module in modules {
        let mut included_targets = TargetSet::new();
        let mut metadatas = vec![];
        // extract unified data
        for d in &module.data {
            if let Some(t) = d.targets.iter().next()
                && included_targets.contains(t)
            {
                continue;
            }
            if d.targets == TargetSet::all() {
                included_targets.extend(d.targets);
                metadatas.push(ModuleMetadata::AllUnified(d));
                continue;
            }
            if d.targets == Target::mac() {
                included_targets.extend(d.targets);
                metadatas.push(ModuleMetadata::MacosUnified(d));
                continue;
            }
            if d.targets == Target::win() {
                included_targets.extend(d.targets);
                metadatas.push(ModuleMetadata::WindowsUnified(d));
                continue;
            }
            if d.targets == Target::linux() {
                included_targets.extend(d.targets);
                metadatas.push(ModuleMetadata::LinuxUnified(d));
                continue;
            }
        }
        // extract remaining non-unified data
        if included_targets.union(Target::win()) != included_targets {
            let mut data_x64 = None;
            let mut data_arm = None;
            for d in &module.data {
                if d.targets == TargetSet::from(Target::WindowsX64) {
                    data_x64 = Some(d);
                    continue;
                }
                if d.targets == TargetSet::from(Target::WindowsArm) {
                    data_arm = Some(d);
                    continue;
                }
            }
            if data_x64.is_some() || data_arm.is_some() {
                included_targets.extend(Target::win());
                metadatas.push(ModuleMetadata::WindowsMux { data_x64, data_arm });
            }
        }
        if included_targets.union(Target::linux()) != included_targets {
            let mut data_pacman = None;
            let mut data_apt = None;
            for d in &module.data {
                if d.targets == TargetSet::from(Target::LinuxPacmanX64) {
                    data_pacman = Some(d);
                    continue;
                }
                if d.targets == TargetSet::from(Target::LinuxAptX64) {
                    data_apt = Some(d);
                    continue;
                }
            }
            if data_pacman.is_some() || data_apt.is_some() {
                included_targets.extend(Target::linux());
                metadatas.push(ModuleMetadata::LinuxMux {
                    data_pacman,
                    data_apt,
                });
            }
        }
        if included_targets != TargetSet::all() {
            metadatas.push(ModuleMetadata::Leftover(included_targets.complement()));
        }
        out.push(metadatas);
    }
    Ok(out)
}

enum ModuleMetadata<'a> {
    AllUnified(&'a ModuleData),
    MacosUnified(&'a ModuleData),
    WindowsUnified(&'a ModuleData),
    LinuxUnified(&'a ModuleData),
    WindowsMux {
        data_x64: Option<&'a ModuleData>,
        data_arm: Option<&'a ModuleData>,
    },
    LinuxMux {
        data_pacman: Option<&'a ModuleData>,
        data_apt: Option<&'a ModuleData>,
    },
    Leftover(TargetSet),
}

impl ModuleMetadata<'_> {
    fn build_metadata(
        &self,
        snake_name: &str,
        kebab_name: &str,
        out: &mut String,
    ) -> cu::Result<()> {
        use std::fmt::Write as _;

        if let Self::Leftover(targets) = self {
            let _ = writeln!(
                out,
                r##"    {cfg_attr} {{ crate::Package::stub("{kebab_name}") }},"##,
                cfg_attr = CfgAttr(*targets).attr(),
            );
            return Ok(());
        }

        let targets = self.metadata_targets();
        let unified_data = self.unified_data();
        let data = self.any_data();
        let module_path = format!("_pkg_{}{}", snake_name, self.unified_module_suffix()?);
        let _ = writeln!(
            out,
            r##"    {cfg_attr} {{ crate::Package {{
        enabled: true,
        name: "{kebab_name}","##,
            cfg_attr = CfgAttr(targets).attr(),
        );

        match unified_data {
            None => {
                let _ = writeln!(out, r##"        binaries_fn: {module_path}::binaries,"##);
            }
            Some(d) => {
                let binaries = d
                    .kebab_binaries
                    .iter()
                    .map(|x| format!("BinId::{}", kebab::kebab_to_pascal(x)))
                    .join(" | ");
                let _ = writeln!(out, "        binaries_fn: || enum_set!{{ {binaries} }},");
            }
        }

        let linux_flavor_set = match self {
            ModuleMetadata::MacosUnified(_)
            | ModuleMetadata::WindowsUnified(_)
            | ModuleMetadata::WindowsMux { .. }
            | ModuleMetadata::Leftover(_) => "corelib::opfs::LinuxFlavor::none()",
            ModuleMetadata::AllUnified(_) |
            ModuleMetadata::LinuxUnified(_) => "corelib::opfs::LinuxFlavor::all()",
            ModuleMetadata::LinuxMux {
                data_pacman,
                data_apt: _,
            } => {
                if data_pacman.is_some() {
                    "enum_set!{ corelib::opfs::LinuxFlavor::Pacman }"
                } else {
                    "enum_set!{ corelib::opfs::LinuxFlavor::Apt }"
                }
            }
        };
        let _ = writeln!(out, "        linux_flavors: {linux_flavor_set},");

        let cpu_arch_set = match self {
            ModuleMetadata::WindowsUnified(_)
            | ModuleMetadata::MacosUnified(_)
            | ModuleMetadata::LinuxUnified(_)
            | ModuleMetadata::AllUnified(_) => "corelib::opfs::CpuArch::all()",
            ModuleMetadata::Leftover(_) => "corelib::opfs::CpuArch::all()",
            ModuleMetadata::WindowsMux {
                data_x64,
                data_arm: _,
            } => {
                if data_x64.is_some() {
                    "enum_set!{ corelib::opfs::CpuArch::X64 }"
                } else {
                    "enum_set!{ corelib::opfs::CpuArch::ARM64 }"
                }
            }
            ModuleMetadata::LinuxMux {
                data_pacman: _,
                data_apt: _,
            } => "corelib::opfs::CpuArch::all()",
        };
        let _ = writeln!(out, "        cpu_archs: {cpu_arch_set},");

        let _ = writeln!(
            out,
            "        short_desc: {},",
            json::stringify(data.short_desc())?
        );
        let _ = writeln!(
            out,
            "        long_desc: {},",
            json::stringify(&data.long_desc())?
        );
        // required functions
        let _ = writeln!(out, "        verify_fn: {module_path}::verify,");
        let _ = writeln!(out, "        install_fn: {module_path}::install,");
        let _ = writeln!(out, "        uninstall_fn: {module_path}::uninstall,");
        macro_rules! write_optional_function {
            ($has_ident:ident, $fn_name:literal, $stub_name:literal) => {
                if unified_data.is_none() || data.$has_ident {
                    let fn_name = $fn_name;
                    let _ = writeln!(out, "        {fn_name}_fn: {module_path}::{fn_name},");
                } else {
                    let _ = writeln!(out, "        {}_fn: _stub::{},", $fn_name, $stub_name);
                }
            };
        }
        write_optional_function!(
            has_binary_dependencies,
            "binary_dependencies",
            "empty_bin_set"
        );
        write_optional_function!(
            has_config_dependencies,
            "config_dependencies",
            "empty_pkg_set"
        );
        write_optional_function!(has_download, "download", "ok");
        write_optional_function!(has_configure, "configure", "ok");
        write_optional_function!(has_clean, "clean", "ok");
        write_optional_function!(has_config_location, "config_location", "ok_none");
        write_optional_function!(has_backup_restore, "backup", "ok");
        write_optional_function!(has_backup_restore, "restore", "ok");
        write_optional_function!(has_pre_uninstall, "pre_uninstall", "ok");

        let _ = writeln!(out, "    }} }},");
        Ok(())
    }
    fn unified_module_suffix(&self) -> cu::Result<&'static str> {
        match self {
            ModuleMetadata::AllUnified(_) => Ok(""),
            ModuleMetadata::MacosUnified(_) => Ok("__mac"),
            ModuleMetadata::WindowsUnified(_) => Ok("__win"),
            ModuleMetadata::LinuxUnified(_) => Ok("__linux"),
            ModuleMetadata::WindowsMux { .. } => Ok("__win"),
            ModuleMetadata::LinuxMux { .. } => Ok("__linux"),
            ModuleMetadata::Leftover(_) => {
                cu::bail!("cannot call module_suffix() on ModuleMetadata::Leftover");
            }
        }
    }

    fn metadata_targets(&self) -> TargetSet {
        match self {
            ModuleMetadata::AllUnified(_) => TargetSet::all(),
            ModuleMetadata::MacosUnified(_) => Target::mac(),
            ModuleMetadata::WindowsUnified(_) => Target::win(),
            ModuleMetadata::LinuxUnified(_) => Target::linux(),
            ModuleMetadata::WindowsMux { .. } => Target::win(),
            ModuleMetadata::LinuxMux { .. } => Target::linux(),
            ModuleMetadata::Leftover(set) => *set,
        }
    }

    fn unified_data(&self) -> Option<&ModuleData> {
        match self {
            ModuleMetadata::AllUnified(d) => Some(d),
            ModuleMetadata::MacosUnified(d) => Some(d),
            ModuleMetadata::WindowsUnified(d) => Some(d),
            ModuleMetadata::LinuxUnified(d) => Some(d),
            ModuleMetadata::WindowsMux { .. } => None,
            ModuleMetadata::LinuxMux { .. } => None,
            ModuleMetadata::Leftover(_) => None,
        }
    }

    fn any_data(&self) -> &ModuleData {
        match self {
            ModuleMetadata::AllUnified(d) => d,
            ModuleMetadata::MacosUnified(d) => d,
            ModuleMetadata::WindowsUnified(d) => d,
            ModuleMetadata::LinuxUnified(d) => d,
            ModuleMetadata::WindowsMux { data_x64, data_arm } => {
                if let Some(d) = data_x64 {
                    return d;
                }
                data_arm.as_ref().unwrap()
            }
            ModuleMetadata::LinuxMux {
                data_pacman,
                data_apt,
            } => {
                if let Some(d) = data_pacman {
                    return d;
                }
                data_apt.as_ref().unwrap()
            }
            ModuleMetadata::Leftover(_) => {
                panic!("cannot call any_data() on ModuleMetadata::Leftover");
            }
        }
    }

    fn build_module(
        &self,
        snake_name: &str,
        registry_src_path: &Path,
        out: &mut String,
    ) -> cu::Result<()> {
        use std::fmt::Write as _;
        let (mux_match_expr, mux_arms) = match self {
            ModuleMetadata::AllUnified(d) => {
                Self::build_mod_decl(d, snake_name, "", registry_src_path, out)?;
                return Ok(());
            }
            ModuleMetadata::MacosUnified(d) => {
                Self::build_mod_decl(d, snake_name, "__mac", registry_src_path, out)?;
                return Ok(());
            }
            ModuleMetadata::WindowsUnified(d) => {
                Self::build_mod_decl(d, snake_name, "__win", registry_src_path, out)?;
                return Ok(());
            }
            ModuleMetadata::LinuxUnified(d) => {
                Self::build_mod_decl(d, snake_name, "__linux", registry_src_path, out)?;
                return Ok(());
            }
            ModuleMetadata::WindowsMux { data_x64, data_arm } => {
                let mut mux_arms = Vec::with_capacity(2);
                if let Some(d) = data_x64 {
                    Self::build_mod_decl(d, snake_name, "__win_x64", registry_src_path, out)?;
                    mux_arms.push(("corelib::opfs::CpuArch::X64", "__win_x64", *d));
                }
                if let Some(d) = data_arm {
                    Self::build_mod_decl(d, snake_name, "__win_arm", registry_src_path, out)?;
                    mux_arms.push(("corelib::opfs::CpuArch::ARM64", "__win_arm", *d));
                }
                let _ = writeln!(
                    out,
                    "{} mod _pkg_{}__win {{ use super::*;",
                    CfgAttr(Target::win()).attr(),
                    snake_name,
                );
                ("corelib::opfs::cpu_arch()", mux_arms)
            }
            ModuleMetadata::LinuxMux {
                data_pacman,
                data_apt,
            } => {
                let mut mux_arms = Vec::with_capacity(2);
                if let Some(d) = data_pacman {
                    Self::build_mod_decl(
                        d,
                        snake_name,
                        "__linux_pacman_x64",
                        registry_src_path,
                        out,
                    )?;
                    mux_arms.push((
                        "corelib::opfs::LinuxFlavor::Pacman",
                        "__linux_pacman_x64",
                        *d,
                    ));
                }
                if let Some(d) = data_apt {
                    Self::build_mod_decl(d, snake_name, "__linux_apt_x64", registry_src_path, out)?;
                    mux_arms.push(("corelib::opfs::LinuxFlavor::Apt", "__linux_apt_x64", *d));
                }
                let _ = writeln!(
                    out,
                    "{} mod _pkg_{}__linux {{ use super::*;",
                    CfgAttr(Target::linux()).attr(),
                    snake_name,
                );
                ("corelib::opfs::linux_flavor()", mux_arms)
            }
            ModuleMetadata::Leftover(_) => {
                return Ok(());
            }
        };
        // mux module
        macro_rules! write_end_match {
            (none) => {
                let _ = writeln!(out, "        _ => Ok(None),\n    }} }}");
            };
            (default) => {
                let _ = writeln!(out, "        _ => Default::default(),\n    }} }}");
            };
            (unreachable) => {
                let _ = writeln!(out, "        _ => cu::bail!(\"unreachable\"),\n    }} }}");
            };
        }
        {
            let _ = writeln!(
                out,
                "    pub fn binaries() -> EnumSet<BinId> {{ match {mux_match_expr} {{",
            );
            for (match_arm, _, d) in &mux_arms {
                let binaries = d
                    .kebab_binaries
                    .iter()
                    .map(|x| format!("BinId::{}", kebab::kebab_to_pascal(x)))
                    .join(" | ");
                let _ = writeln!(out, "        {match_arm} => enum_set!{{ {binaries} }},",);
            }
            write_end_match!(default);
        }
        {
            let _ = writeln!(
                out,
                "    pub fn config_location(_ctx: &crate::Context) -> cu::Result<Option<std::path::PathBuf>> {{ match {mux_match_expr} {{"
            );
            for (match_arm, suffix, d) in &mux_arms {
                if d.has_config_location {
                    let _ = writeln!(
                        out,
                        "        {match_arm} => _pkg_{snake_name}{suffix}::config_location(_ctx),",
                    );
                } else {
                    let _ = writeln!(out, "        {match_arm} => Ok(None),");
                }
            }
            write_end_match!(none);
        }
        for fn_name in ["verify", "install", "uninstall"] {
            let retty = if fn_name == "verify" {
                "crate::Verified"
            } else {
                "()"
            };
            let _ = writeln!(
                out,
                "    pub fn {fn_name}(ctx: &crate::Context) -> cu::Result<{retty}> {{ match {mux_match_expr} {{"
            );
            for (match_arm, suffix, _) in &mux_arms {
                let _ = writeln!(
                    out,
                    "        {match_arm} => _pkg_{snake_name}{suffix}::{fn_name}(ctx),",
                );
            }
            // the package functions will check the flavor before invoking vtable functions,
            // so the other platforms are not reachable
            write_end_match!(unreachable);
        }
        macro_rules! write_optional_function {
            ($fn_name:literal, $has_ident:ident) => {
                let _ = writeln!(
                    out,
                    "    pub fn {}(_ctx: &crate::Context) -> cu::Result<()> {{ match {} {{",
                    $fn_name, mux_match_expr
                );
                for (match_arm, suffix, d) in &mux_arms {
                    if d.$has_ident {
                        let _ = writeln!(
                            out,
                            "        {match_arm} => _pkg_{snake_name}{suffix}::{fn_name}(_ctx),",
                            fn_name = $fn_name
                        );
                    } else {
                        let _ = writeln!(out, "        {match_arm} => Ok(()),");
                    }
                }
                write_end_match!(unreachable);
            };
        }
        write_optional_function!("download", has_download);
        write_optional_function!("configure", has_configure);
        write_optional_function!("clean", has_clean);
        write_optional_function!("backup", has_backup_restore);
        write_optional_function!("restore", has_backup_restore);
        write_optional_function!("pre_uninstall", has_pre_uninstall);
        macro_rules! write_dependency_function {
            ($fn_name:literal, $has_ident:ident, $retty:literal) => {
                let _ = writeln!(
                    out,
                    "    pub fn {}() -> {} {{ match {} {{",
                    $fn_name, $retty, mux_match_expr
                );
                for (match_arm, suffix, d) in &mux_arms {
                    if d.$has_ident {
                        let _ = writeln!(
                            out,
                            "        {match_arm} => _pkg_{snake_name}{suffix}::{fn_name}(),",
                            fn_name = $fn_name
                        );
                    } else {
                        let _ = writeln!(out, "        {match_arm} => Default::default(),",);
                    }
                }
                write_end_match!(default);
            };
        }
        write_dependency_function!(
            "binary_dependencies",
            has_binary_dependencies,
            "enumset::EnumSet<super::BinId>"
        );
        write_dependency_function!(
            "config_dependencies",
            has_config_dependencies,
            "enumset::EnumSet<super::PkgId>"
        );

        let _ = writeln!(out, "}}");

        Ok(())
    }

    fn build_mod_decl(
        data: &ModuleData,
        snake_name: &str,
        suffix: &str,
        registry_src_path: &Path,
        out: &mut String,
    ) -> cu::Result<()> {
        use std::fmt::Write as _;
        let path = data.file.normalize()?;
        let path = path.try_to_rel_from(registry_src_path);
        // relative path ensures the output is consistent throughout
        // build environments
        cu::ensure!(path.is_relative(), "'{}'", path.display())?;
        let path = path.as_utf8()?;
        let _ = writeln!(
            out,
            "{} #[path = {}] mod _pkg_{}{};",
            CfgAttr(data.targets).attr(),
            json::stringify(path)?,
            snake_name,
            suffix
        );
        Ok(())
    }
}

fn generate_id_enum(
    out: &mut String,
    prefix: &str,
    kebab_values: &[&String],
    pascal_values: &[String],
) -> cu::Result<()> {
    use std::fmt::Write as _;

    writeln!(out, "#[derive(Debug, EnumSetType, Enum)]")?;
    writeln!(out, "pub enum {prefix}Id {{")?;
    for name in pascal_values {
        writeln!(out, "    {name},")?;
    }
    writeln!(out, "}}")?;
    let upper_prefix = prefix.to_ascii_uppercase();
    writeln!(
        out,
        "static {upper_prefix}_NAMES: phf::Map<&'static str, {prefix}Id> = phf::phf_map! {{"
    )?;
    for (kebab, pascal) in std::iter::zip(kebab_values, pascal_values) {
        writeln!(out, "    \"{kebab}\" => {prefix}Id::{pascal},")?;
    }
    writeln!(out, "}};")?;
    writeln!(out, "impl {prefix}Id {{")?;
    writeln!(
        out,
        "    /// Convert kebab-case name to enum\n    pub fn from_str(name: &str) -> Option<Self> {{ {upper_prefix}_NAMES.get(name).copied() }}"
    )?;
    writeln!(
        out,
        "    /// Get kebab-case name\n    pub fn to_str(self) -> &'static str {{ match self {{"
    )?;
    for (kebab, pascal) in std::iter::zip(kebab_values, pascal_values) {
        writeln!(out, "            Self::{pascal} => \"{kebab}\",")?;
    }
    writeln!(out, "        }}\n    }}\n}}")?;

    writeln!(
        out,
        r"
impl std::fmt::Display for {prefix}Id {{
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {{
        self.to_str().fmt(f)
    }}
}}
    "
    )?;

    Ok(())
}

fn build_bin_providers(
    pascal_bins: &[String],
    kebab_bins: &[&String],
    pascal_pkgs: &[String],
    modules: &[ParsedModule],
    out: &mut String,
) -> cu::Result<()> {
    use std::fmt::Write as _;

    for (pascal_bin, kebab_bin) in std::iter::zip(pascal_bins, kebab_bins) {
        // providers for this binary
        let mut providers: BTreeMap<Target, BTreeSet<String>> = BTreeMap::default();
        for (pascal_pkg, m) in std::iter::zip(pascal_pkgs, modules) {
            for d in &m.data {
                if !d.kebab_binaries.contains(kebab_bin.as_str()) {
                    continue;
                }
                for t in d.targets {
                    let set = providers.entry(t).or_default();
                    set.insert(format!("PkgId::{pascal_pkg}"));
                }
            }
        }

        let providers = Target::combine_targets(providers);
        let _ = writeln!(out, "            Self::{pascal_bin} => {{");
        let mut providers_linux_pacman_x64 = None;
        let mut providers_linux_apt_x64 = None;
        let mut providers_win_x64 = None;
        let mut providers_win_arm = None;
        for (targets, providers) in &providers {
            let mut targets = *targets;
            if targets == TargetSet::all() {
                let _ = writeln!(
                    out,
                    "                enum_set!{{ {} }}",
                    providers.iter().join(" | "),
                );
                continue;
            }
            if targets.intersection(Target::linux()) != Target::linux() {
                if targets.contains(Target::LinuxPacmanX64) {
                    providers_linux_pacman_x64 = Some(providers);
                    targets.remove(Target::LinuxPacmanX64);
                }
                if targets.contains(Target::LinuxAptX64) {
                    providers_linux_apt_x64 = Some(providers);
                    targets.remove(Target::LinuxAptX64);
                }
            }
            if targets.intersection(Target::win()) != Target::win() {
                if targets.contains(Target::WindowsX64) {
                    providers_win_x64 = Some(providers);
                    targets.remove(Target::WindowsX64);
                    continue;
                }
                if targets.contains(Target::WindowsArm) {
                    providers_win_arm = Some(providers);
                    targets.remove(Target::WindowsArm);
                    continue;
                }
            }
            if targets.is_empty() {
                continue;
            }
            if targets == Target::win() {
                let _ = writeln!(
                    out,
                    "                #[cfg(windows)] {{ enum_set!{{ {} }} }}",
                    providers.iter().join(" | "),
                );
                continue;
            }
            if targets == Target::linux() {
                let _ = writeln!(
                    out,
                    "                #[cfg(target_os = \"linux\")] {{ enum_set!{{ {} }} }}",
                    providers.iter().join(" | "),
                );
                continue;
            }
            if targets == Target::mac() {
                let _ = writeln!(
                    out,
                    "                #[cfg(target_os = \"macos\")] {{ enum_set!{{ {} }} }}",
                    providers.iter().join(" | "),
                );
                continue;
            }
            let _ = writeln!(
                out,
                "                {} {{ enum_set!{{ {} }}}}",
                CfgAttr(targets).attr(),
                providers.iter().join(" | "),
            );
        }
        if providers_linux_pacman_x64.is_some() || providers_linux_apt_x64.is_some() {
            let _ = writeln!(
                out,
                "                {} {{ match corelib::opfs::linux_flavor() {{",
                CfgAttr(Target::linux()).attr(),
            );
            let mut count = 0;
            if let Some(providers) = providers_linux_pacman_x64 {
                let _ = writeln!(
                    out,
                    "                    corelib::opfs::LinuxFlavor::Pacman => enum_set! {{ {} }},",
                    providers.iter().join(" | ")
                );
                count += 1;
            }
            if let Some(providers) = providers_linux_apt_x64 {
                let _ = writeln!(
                    out,
                    "                    corelib::opfs::LinuxFlavor::Apt => enum_set! {{ {} }},",
                    providers.iter().join(" | ")
                );
                count += 1;
            }
            if count != 2 {
                let _ = writeln!(out, "                    _ => enum_set!{{}},");
            }
            let _ = writeln!(out, "                }} }}");
        }
        if providers_win_x64.is_some() || providers_win_arm.is_some() {
            let _ = writeln!(
                out,
                "                {} {{ match corelib::opfs::cpu_arch() {{",
                CfgAttr(Target::win()).attr(),
            );
            let mut count = 0;
            if let Some(providers) = providers_win_x64 {
                let _ = writeln!(
                    out,
                    "                    corelib::opfs::CpuArch::X64 => enum_set! {{ {} }},",
                    providers.iter().join(" | ")
                );
                count += 1;
            }
            if let Some(providers) = providers_win_arm {
                let _ = writeln!(
                    out,
                    "                    corelib::opfs::CpuArch::ARM64 => enum_set! {{ {} }},",
                    providers.iter().join(" | ")
                );
                count += 1;
            }
            if count != 2 {
                let _ = writeln!(out, "                    _ => enum_set!{{}},");
            }
            let _ = writeln!(out, "                }} }}");
        }
        writeln!(out, "            }}")?;
    }

    Ok(())
}

